import java.util.*;
public class switchwala {
    public static void main(String args[]){
        int number = 8;
        switch (number){
            case 8 :System.out.println("samosa");
            break;
            case 1 :System.out.println("samosa2");
            break;
            default:System.out.println("samosa3");
        }
    }
    
}
